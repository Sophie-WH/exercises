# Function for creating tables

def print_three_header(a, b, c):
    print(a + "\t\t" + b + "\t\t" + c)
    print("__________________________________________________________")
def print_two_header(a, b):
    print(a + "\t\t" + b)
    print("__________________________________________________________")
def print_one_header(a):
    print(a)
    print("__________________________________________________________")